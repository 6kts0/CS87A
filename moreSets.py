# Add a unique element to a set using .add
myset = {1, 2, 3}
add_numbers = myset.add(2)

print(f"Appending 2 to a set of 1, 2, 3: {myset}") 

# Update a set with unique values using .update
letters = {'a', 'b', 'c'}
update_letters = letters.update('c', 'd')

print(f"Updating a set of a, b, c with c, d: {letters}")

